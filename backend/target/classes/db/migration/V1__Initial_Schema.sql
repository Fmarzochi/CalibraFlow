CREATE TABLE tenants (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255) NOT NULL UNIQUE,
    document VARCHAR(255) NOT NULL UNIQUE,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    is_active BOOLEAN NOT NULL DEFAULT TRUE
);

CREATE TABLE users (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    email VARCHAR(255) NOT NULL UNIQUE,
    password VARCHAR(255) NOT NULL,
    cpf VARCHAR(14) UNIQUE,
    role VARCHAR(50) NOT NULL,
    is_enabled BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_users_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id)
);

CREATE TABLE user_permissions (
    user_id BIGINT NOT NULL,
    permission VARCHAR(50) NOT NULL,
    CONSTRAINT fk_user_permissions_user FOREIGN KEY (user_id) REFERENCES users (id) ON DELETE CASCADE,
    UNIQUE (user_id, permission)
);

CREATE TABLE categories (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    validity_days INTEGER,
    description VARCHAR(255),
    CONSTRAINT fk_categories_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
    CONSTRAINT uk_categories_tenant_name UNIQUE (tenant_id, name)
);

CREATE TABLE locations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    name VARCHAR(255) NOT NULL,
    description VARCHAR(255),
    active BOOLEAN NOT NULL DEFAULT TRUE,
    CONSTRAINT fk_locations_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
    CONSTRAINT uk_locations_tenant_name UNIQUE (tenant_id, name)
);

CREATE TABLE periodicities (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    category_id BIGINT NOT NULL,
    days INTEGER NOT NULL,
    CONSTRAINT fk_periodicities_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
    CONSTRAINT fk_periodicities_category FOREIGN KEY (category_id) REFERENCES categories(id)
);

CREATE TABLE instruments (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    tag VARCHAR(255) NOT NULL,
    name VARCHAR(255) NOT NULL,
    serial_number VARCHAR(255),
    manufacturer VARCHAR(255),
    model VARCHAR(255),
    location_id BIGINT NOT NULL,
    tolerance VARCHAR(255),
    status VARCHAR(50) NOT NULL DEFAULT 'ATIVO',
    is_active BOOLEAN NOT NULL DEFAULT TRUE,
    category_id BIGINT,
    periodicity_id BIGINT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_instruments_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
    CONSTRAINT fk_instruments_location FOREIGN KEY (location_id) REFERENCES locations(id),
    CONSTRAINT fk_instruments_category FOREIGN KEY (category_id) REFERENCES categories(id),
    CONSTRAINT fk_instruments_periodicity FOREIGN KEY (periodicity_id) REFERENCES periodicities(id),
    CONSTRAINT uk_instruments_tenant_tag UNIQUE (tenant_id, tag)
);

CREATE TABLE calibrations (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    instrument_id BIGINT NOT NULL,
    calibration_date DATE NOT NULL,
    next_calibration_date DATE,
    certificate_number VARCHAR(255) NOT NULL,
    approved BOOLEAN NOT NULL,
    observations TEXT,
    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    created_by_id BIGINT NOT NULL,
    created_by_name VARCHAR(255) NOT NULL,
    CONSTRAINT fk_calibrations_tenant FOREIGN KEY (tenant_id) REFERENCES tenants(id),
    CONSTRAINT fk_calibrations_instrument FOREIGN KEY (instrument_id) REFERENCES instruments(id)
);

CREATE TABLE certificates (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    tenant_id BIGINT NOT NULL,
    calibration_id BIGINT NOT NULL,
    original_file_name VARCHAR(255) NOT NULL,
    storage_path VARCHAR(500) NOT NULL UNIQUE,
    content_type VARCHAR(100) NOT NULL,
    file_size BIGINT NOT NULL,
    uploaded_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    uploaded_by_id BIGINT NOT NULL,
    uploaded_by_name VARCHAR(255) NOT NULL,
    CONSTRAINT fk_certificates_tenant FOREIGN KEY (tenant_id) REFERENCES tenants (id),
    CONSTRAINT fk_certificates_calibration FOREIGN KEY (calibration_id) REFERENCES calibrations (id)
);

CREATE TABLE instrument_status_history (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    instrument_id BIGINT NOT NULL,
    tenant_id BIGINT NOT NULL,
    previous_status VARCHAR(50) NOT NULL,
    new_status VARCHAR(50) NOT NULL,
    changed_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
    responsible_id BIGINT NOT NULL,
    responsible_full_name VARCHAR(255) NOT NULL,
    responsible_cpf VARCHAR(14) NOT NULL,
    source_ip VARCHAR(45),
    justification TEXT NOT NULL,
    CONSTRAINT fk_history_instrument FOREIGN KEY (instrument_id) REFERENCES instruments (id),
    CONSTRAINT fk_history_tenant FOREIGN KEY (tenant_id) REFERENCES tenants (id)
);

CREATE TABLE refresh_tokens (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id BIGINT NOT NULL UNIQUE,
    token VARCHAR(255) NOT NULL UNIQUE,
    expiry_date TIMESTAMP NOT NULL,
    CONSTRAINT fk_refresh_tokens_user FOREIGN KEY (user_id) REFERENCES users(id)
);

CREATE TABLE movements (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    instrument_id BIGINT NOT NULL,
    origin_id BIGINT,
    destination_id BIGINT,
    user_id BIGINT NOT NULL,
    movement_date TIMESTAMP NOT NULL,
    reason TEXT,
    CONSTRAINT fk_movements_instrument FOREIGN KEY (instrument_id) REFERENCES instruments(id),
    CONSTRAINT fk_movements_origin FOREIGN KEY (origin_id) REFERENCES locations(id),
    CONSTRAINT fk_movements_destination FOREIGN KEY (destination_id) REFERENCES locations(id),
    CONSTRAINT fk_movements_user FOREIGN KEY (user_id) REFERENCES users(id)
);

